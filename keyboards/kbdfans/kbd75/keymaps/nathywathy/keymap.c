/*
 * Features:
 * Toggle settings:
 * - FN + ALT = Toggle swapping ALT with GUI
 * - FN + GUI = Toggle disabling GUI
 * - FN + CAPS = Toggle changing CAPS to LCTRL
 * - FN + F12 = Toggle locking all keys
 *
 *
 * Mouse controls:
 * - RCTRL + WASD = Mouse control
 * - RCTRL + Q = Mouse left click
 * - RCTRL + E = Mouse right click
 *
 * - RCTRL + UP/DOWN/LEFT/RIGHT = Mouse controls
 * - RCTRL + LSHIFT = Mouse left click
 * - RCTRL + END = Mouse right click
 *
 * - RCTRL + 1 = Mouse speed 1
 * - RCTRL + 2 = Mouse speed 2
 * - RCTRL + 3 = Mouse speed 3
*/

